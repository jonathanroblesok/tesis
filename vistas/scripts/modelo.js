//funcion para guardaryeditar
function guardaryeditar(e){
     e.preventDefault();//no se activara la accion predeterminada 
     //$("#btnGuardar").prop("disabled",true);
     var formData=new FormData($("#formulario")[0]);

     $.ajax({
     	url: "../ajax/modelo.php?op=guardaryeditar",
     	type: "POST",
     	data: formData,
     	contentType: false,
     	processData: false,

     	success: function(datos){
     		bootbox.alert(datos);
     		mostrarform(false);
     		listar();
     	}
     });

     limpiar();
}
	
function mostrar(idmodelo){
	$.post("../ajax/modelo.php?op=mostrar",{idmodelo : idmodelo},
		function(data,status)
		{
			data=JSON.parse(data);
			mostrarform(true);

			$("#talla_id").val(data.talla_id);
			$("#talla_id").selectpicker('refresh');
			$("#categoria_id").val(data.categoria_id);
			$("#categoria_id").selectpicker('refresh');

			$("#fecha").val(data.fecha);
			$("#idmodelo").val(data.idmodelo);
			
			//ocultar y mostrar los botones
			$("#btnGuardar").hide();
			$("#btnCancelar").show();
			$("#btnAgregarArt").hide();
		});
	$.post("../ajax/modelo.php?op=listarDetalle&id="+idmodelo,function(r){
		$("#detalles").html(r);
	});

}


//funcion para desactivar
function anular(idmodelo){
	bootbox.confirm("¿Esta seguro de desactivar este dato?", function(result){
		if (result) {
			$.post("../ajax/modelo.php?op=anular", {idmodelo : idmodelo}, function(e){
				bootbox.alert(e);
				tabla.ajax.reload();
			});
		}
	})
}

